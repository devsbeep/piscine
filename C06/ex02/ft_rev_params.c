#include <unistd.h>

int	main(int ac, char **ag)
{
	int	i;
	int	j;

	i = ac - 1;
	if (ac == 0)
		return (0);

	while (i > 0)
	{
		j = 0;
		while (ag[i][j] != '\0')
		{
			write(1, &ag[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		i--;
	}
}
