#include <unistd.h>

int	main(int ac, char **ag)
{
	int	i;
	int	j;

	i = 1;
	while (i < ac)
	{
		j = 0;
		while (ag[i][j])
		{
			write(1, &ag[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
}
