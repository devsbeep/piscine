#include <unistd.h>

void	ft_putnbr(int nb)
{
	long	i;
	char	c;

	i = nb;
	if (i < 0)
	{
		write(1, "-", 1);
		i = -i;
	}
	if (i <= 9)
	{
		c = i + '0';
		write(1, &c, 1);
	}
	if (i > 9)
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
}
